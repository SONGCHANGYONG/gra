	public  int 총점계산(int[] 국어성적, int 학생수 ) {//메소드에 리턴값 1개가 필요 void느 리턴값 없음
		int 총점 = 0 ; //지역변수 선언              //메소드() 밖에서 불어올 값들을 입력해준다.
		for (int i = 0; i < 학생수; i++) {
			총점 =총점 +국어성적[i];
		}
		
		return 총점;
	}
	
