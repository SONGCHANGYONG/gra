	public  int[] 성적입력(int 학생수) { //배출할 함수형을써주면됨 int[] int형 배열
		
		int[] 국어성적 = new int[학생수]; 
		Scanner sc = new Scanner(System.in);
		
		
		for (int i = 0; i < 학생수; i++) {
			System.out.println(i+"번, 국어성적을 입력 해 주세요");
			국어성적[i] = sc.nextInt();
		}
		return 국어성적;//대표명만 기입하면됨
	}
	
